// ===== Vimeo 5s loop segment with soft fade (startAt = 7s) =====
(function initVimeoLoop(){
  const iframe = document.getElementById('heroVimeo');
  if(!iframe) return;
  const startAt = 7;      // seconds
  const SEGMENT = 5;      // seconds
  const FADE_DURATION = 0.6; // seconds

  const container = iframe.parentElement;
  container.style.position = 'relative';
  const fadeOverlay = document.createElement('div');
  Object.assign(fadeOverlay.style, {
    position:'absolute', inset:'0', pointerEvents:'none', opacity:'0',
    transition:`opacity ${FADE_DURATION}s ease`, background:'#fff'
  });
  container.appendChild(fadeOverlay);

  const onApi = ()=>{
    const player = new Vimeo.Player(iframe);
    player.setLoop(false); player.setMuted(true);
    player.setCurrentTime(startAt).catch(()=>{});
    player.play().catch(()=>{});
    let fading = false;
    player.on('timeupdate', (e)=>{
      if(e.seconds >= startAt + SEGMENT - 0.5 && !fading){ fading = true; fadeOverlay.style.opacity = '1'; }
      if(e.seconds >= startAt + SEGMENT){ player.setCurrentTime(startAt).then(()=>{ fadeOverlay.style.opacity = '0'; fading = false; }); }
    });
  };
  if(typeof Vimeo === 'undefined' || !Vimeo.Player){
    const s=document.createElement('script'); s.src='https://player.vimeo.com/api/player.js'; s.onload=onApi; document.head.appendChild(s);
  } else { onApi(); }
})();

